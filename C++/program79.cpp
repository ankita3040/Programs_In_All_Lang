// Iteration with Sequence 

#include<iostream>
using namespace std;

void Display()
{
    int j = 0;
    
    for(j =1; j <= 3; j++)
    {
        cout<<"*"<<'\t'; 
    }
    cout<<'\n';

    for(j =1; j <= 3; j++)
    {
        cout<<"*"<<'\t'; 
    }
    cout<<'\n';

    for(j =1; j <= 3; j++)
    {
        cout<<"*"<<'\t'; 
    }
    cout<<'\n';


}

int main()
{

    Display();

    return 0;
}